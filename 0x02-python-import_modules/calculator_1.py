#!/usr/bin/python3

def add(a, b):
    """ Addition function

    Args:
        a: first number
        b: second number
    Return:
        the returned value. a + b
    """
    return (a + b)

def mul(a, b):
    """ Mulplication function

    Args:
        a: first number
        b: second number

    Returns:
        the results is. a * b
    """
    return (a * b)

def sub(a, b):
    """ Substraction function

    Args:
        a: first number
        b: second number

    Returns:
        the results is. a - b
    """
    return (a - b)

def div(a, b):
    """ Division function

    Args:
        a: first number
        b: second number

    Returns:
        the results is. a / b
    """
    return (a / b)
